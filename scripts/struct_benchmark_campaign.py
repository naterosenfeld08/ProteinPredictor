#!/usr/bin/env python3
"""
Build a scalable WT/mutant benchmark campaign plan.

This script discovers benchmark pairs once, then emits:
1) a canonical manifest (up to target-pairs),
2) split manifests for batch execution (e.g., 10 cases per batch),
3) a runnable shell script template with benchmark_run commands.
"""

from __future__ import annotations

import argparse
from dataclasses import asdict
from datetime import datetime
import json
from pathlib import Path
import sys
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from petase_design.struct_benchmark_discovery import DiscoveryReport, discover_wt_mutant_pairs, write_discovery_manifest


def _utc_now() -> str:
    return datetime.utcnow().replace(microsecond=0).isoformat() + "Z"


def _chunked(seq: list[Any], n: int) -> list[list[Any]]:
    return [seq[i : i + n] for i in range(0, len(seq), n)]


def main() -> None:
    ap = argparse.ArgumentParser(description="Create a 50-pair benchmark campaign plan and split manifests.")
    ap.add_argument("--out-dir", type=Path, default=Path("struct_benchmark_runs") / "campaign_50_pairs")
    ap.add_argument("--target-pairs", type=int, default=50)
    ap.add_argument("--batch-size", type=int, default=10, help="Pairs per split manifest for staged benchmark runs")
    ap.add_argument("--max-entries", type=int, default=4000)
    ap.add_argument("--max-enzymes", type=int, default=60)
    ap.add_argument("--max-pairs-per-enzyme", type=int, default=3)
    ap.add_argument("--resolution-max", type=float, default=3.2)
    ap.add_argument("--min-seq-identity", type=float, default=0.85)
    ap.add_argument("--max-len-delta-frac", type=float, default=0.15)
    ap.add_argument("--disable-sequence-diff-fallback", action="store_true")
    args = ap.parse_args()

    if int(args.target_pairs) < 1:
        raise SystemExit("--target-pairs must be >= 1")
    if int(args.batch_size) < 1:
        raise SystemExit("--batch-size must be >= 1")

    out_dir = args.out_dir
    out_dir.mkdir(parents=True, exist_ok=True)
    manifests_dir = out_dir / "manifests"
    manifests_dir.mkdir(parents=True, exist_ok=True)

    report = discover_wt_mutant_pairs(
        max_entries=int(args.max_entries),
        max_enzymes=int(args.max_enzymes),
        max_pairs_per_enzyme=int(args.max_pairs_per_enzyme),
        resolution_max_a=float(args.resolution_max),
        min_seq_identity=float(args.min_seq_identity),
        max_len_delta_frac=float(args.max_len_delta_frac),
        enable_sequence_diff_fallback=not bool(args.disable_sequence_diff_fallback),
    )

    capped_pairs = list(report.pairs)[: int(args.target_pairs)]
    capped_report = DiscoveryReport(pairs=capped_pairs, exclusions=report.exclusions, stats=dict(report.stats))
    capped_report.stats["target_pairs"] = int(args.target_pairs)
    capped_report.stats["selected_pairs_capped"] = len(capped_pairs)
    capped_report.stats["selected_enzymes_capped"] = len({p.enzyme_key for p in capped_pairs})

    canonical_manifest = manifests_dir / "campaign_manifest.json"
    write_discovery_manifest(capped_report, canonical_manifest)

    split_manifests: list[Path] = []
    for idx, chunk in enumerate(_chunked(capped_pairs, int(args.batch_size)), start=1):
        payload = {
            "schema_version": "2026-05-02.struct-benchmark.campaign-split.v1",
            "generated_at": _utc_now(),
            "stats": {
                "batch_index": idx,
                "batch_size": len(chunk),
                "target_pairs": int(args.target_pairs),
            },
            "pairs": [asdict(p) for p in chunk],
            "exclusions": [],
        }
        out_path = manifests_dir / f"campaign_manifest_batch_{idx:02d}.json"
        out_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
        split_manifests.append(out_path)

    run_script = out_dir / "run_colabfold_batches.sh"
    lines = [
        "#!/usr/bin/env bash",
        "set -euo pipefail",
        "",
        "# Generated by scripts/struct_benchmark_campaign.py",
        "# Run from repository root. Requires local colabfold_batch.",
        "",
    ]
    for idx, manifest_path in enumerate(split_manifests, start=1):
        lines.extend(
            [
                f"python -m petase_design.benchmark_run \\",
                f"  --manifest-json \"{manifest_path}\" \\",
                f"  --out-dir \"{out_dir / f'run_batch_{idx:02d}'}\" \\",
                "  --colabfold \\",
                "  --skip-controls \\",
                "  --gdt-ts-min 50 \\",
                "  --coverage-min 95",
                "",
            ]
        )
    run_script.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
    run_script.chmod(0o755)

    summary = {
        "schema_version": "2026-05-02.struct-benchmark.campaign.v1",
        "generated_at": _utc_now(),
        "counts": {
            "pairs_discovered_raw": len(report.pairs),
            "pairs_in_campaign": len(capped_pairs),
            "enzymes_in_campaign": len({p.enzyme_key for p in capped_pairs}),
            "batch_count": len(split_manifests),
            "batch_size": int(args.batch_size),
        },
        "discovery_stats": report.stats,
        "files": {
            "canonical_manifest": str(canonical_manifest),
            "run_script": str(run_script),
            "split_manifests": [str(p) for p in split_manifests],
        },
    }
    (out_dir / "campaign_summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")
    print(
        f"[campaign] discovered={len(report.pairs)} selected={len(capped_pairs)} "
        f"enzymes={len({p.enzyme_key for p in capped_pairs})} batches={len(split_manifests)}"
    )
    print(f"[campaign] outputs under: {out_dir}")


if __name__ == "__main__":
    main()
